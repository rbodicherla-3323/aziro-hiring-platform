def _infer_role_from_filename(source_filename: str, available_roles):
    raw = str(source_filename or "").strip()
    if not raw:
        return None, ""

    stem = raw.rsplit("/", 1)[-1].rsplit("\\", 1)[-1]
    if "." in stem:
        stem = stem.rsplit(".", 1)[0]

    normalized_stem = _normalize_label(stem)
    compact = _compact_text(normalized_stem)
    if not compact:
        return None, ""

    key_to_label = {}
    for label in available_roles:
        role_key = normalize_role(label)
        if role_key:
            key_to_label[role_key] = label

    def has(token: str):
        return _compact_text(token) in compact

    candidates = []
    if has("javascript") and (has("entry") or has("0-2")):
        candidates.append("js_entry")
    if has("python") and (has("entry") or has("0-2")):
        candidates.append("python_entry")
    if has("java") and (not has("javascript")) and (has("entry") or has("0-2")):
        candidates.append("java_entry")
    if has("java") and has("aws"):
        candidates.append("java_aws")
    if has("python") and has("qa") and has("linux"):
        candidates.append("python_qa_linux")
    if has("python") and has("qa"):
        candidates.append("python_qa")
    if has("python") and (has("development") or has("developer") or has("dev")):
        candidates.append("python_dev")
    if has("python") and (has("ai") or has("ml")):
        candidates.append("python_ai_ml")
    if has("java") and has("qa"):
        candidates.append("java_qa")
    if has("bmc"):
        candidates.append("bmc_engineer")
    if has("kernel") or has("driver"):
        candidates.append("linux_kernel_dd")
    if has("architect") or has("cpp") or has("c++"):
        candidates.append("systems_architect_cpp")

    for role_key in candidates:
        label = key_to_label.get(role_key)
        if label:
            return label, stem

    return None, stem


def _resolve_ai_role_label(ai_role_raw, normalized_available, key_to_label):
    raw = str(ai_role_raw or "").strip()
    if not raw:
        return None

    normalized_ai = _normalize_label(raw)
    if normalized_ai in normalized_available:
        return normalized_available[normalized_ai]

    direct_role_key = normalize_role(raw)
    if direct_role_key and direct_role_key in key_to_label:
        return key_to_label[direct_role_key]

    compact_ai = _compact_text(normalized_ai)
    if not compact_ai:
        return None

    best_label = None
    best_score = 0
    ai_tokens = set(re.findall(r"[a-z]+", normalized_ai))

    for role_key, label in key_to_label.items():
        normalized_label = _normalize_label(label)
        compact_label = _compact_text(normalized_label)
        compact_key = _compact_text(role_key.replace("_", " "))

        score = 0
        if compact_ai == compact_label or compact_ai == compact_key:
            score = 5
        elif compact_ai in compact_label or compact_ai in compact_key:
            score = 4
        elif compact_label in compact_ai or compact_key in compact_ai:
            score = 3
        else:
            label_tokens = set(re.findall(r"[a-z]+", normalized_label.split("(")[0]))
            overlap = len(ai_tokens & label_tokens)
            if overlap >= 2:
                score = 2

        if score > best_score:
            best_score = score
            best_label = label

    if best_score >= 3:
        return best_label
    return None


def match_role_from_jd(
    text: str,
    available_roles,
    use_ai_fallback: bool = True,
    source_file_bytes: bytes = None,
    source_mime_type: str = None,
    text_quality: float = 1.0,
    source_filename: str = None,
):
    warnings = []
    normalized_available = {}
    key_to_label = {}
    for label in available_roles:
        normalized_label = _normalize_label(label)
        role_key = normalize_role(label)
        if not role_key:
            continue
        normalized_available[normalized_label] = label
        key_to_label[role_key] = label

    if not key_to_label:
        return {
            "role": None,
            "role_found": False,
            "message": "No role fits perfectly among available roles.",
            "confidence": {"score": 0, "normalized": 0.0, "margin": 0.0},
            "top_matches": [],
            "warnings": ["No valid roles were provided for JD matching."],
            "used_ai": False,
        }

    quality = float(text_quality or 0.0)
    can_use_deterministic = quality >= _MIN_TEXT_QUALITY_FOR_DETERMINISTIC

    text_lower = str(text or "").lower()
    compact_source = _compact_text(text_lower)
    years = _extract_years(text_lower) if can_use_deterministic else None

    family_strength = {}
    for family, keywords in _FAMILY_KEYWORDS.items():
        family_strength[family] = _count_hits(text_lower, keywords, compact_source) if can_use_deterministic else 0
    dominant_family = max(family_strength, key=family_strength.get) if family_strength else None
    dominant_strength = family_strength.get(dominant_family, 0) if dominant_family else 0

    role_scores = []
    for role_key, label in key_to_label.items():
        profile = _ROLE_PROFILES.get(role_key, {})
        if can_use_deterministic:
            title_hits = _count_hits(text_lower, profile.get("title_phrases", []), compact_source)
            skill_hits = _count_hits(text_lower, profile.get("skills", []), compact_source)
            seniority_hits = _count_hits(text_lower, profile.get("seniority", []), compact_source)
            family = profile.get("family")
            family_hits = family_strength.get(family, 0)

            score = (title_hits * 4) + (skill_hits * 2) + (seniority_hits * 2) + min(family_hits, 3)

            expected_years = profile.get("years")
            if years is not None and expected_years:
                min_years, max_years = expected_years
                if years < min_years:
                    score -= 3
                if years > max_years + 1:
                    score -= 2

            if dominant_family and dominant_strength >= 3 and family != dominant_family:
                score -= 3
        else:
            title_hits = 0
            skill_hits = 0
            seniority_hits = 0
            score = 0

        role_scores.append(
            {
                "role": label,
                "role_key": role_key,
                "score": score,
                "title_hits": title_hits,
                "skill_hits": skill_hits,
                "seniority_hits": seniority_hits,
            }
        )

    role_scores.sort(key=lambda item: item["score"], reverse=True)
    top_matches = [{"role": item["role"], "score": item["score"]} for item in role_scores[:3]]
    top_score = role_scores[0]["score"] if role_scores else 0
    second_score = role_scores[1]["score"] if len(role_scores) > 1 else 0
    margin = top_score - second_score
    normalized_conf = max(0.0, min(1.0, (0.7 * (top_score / 12.0)) + (0.3 * (margin / 6.0))))

    role = None
    role_found = False
    message = "No role fits perfectly among available roles."
    used_ai = False

    if (can_use_deterministic and top_score >= 5 and margin >= 1 and normalized_conf >= 0.50):
        role = role_scores[0]["role"]
        role_found = True
        message = ""
    elif use_ai_fallback:
        if not can_use_deterministic:
            warnings.append("JD text quality is low; deterministic role match disabled.")
        ai_data = _ai_jd_role_match(text, [item["role"] for item in role_scores])
        if ai_data:
            used_ai = True
            ai_role_raw = ai_data.get("role")
            ai_conf = max(0.0, min(1.0, float(ai_data.get("confidence") or 0.0)))
            ai_reason = ai_data.get("reason") or ""
            resolved_ai_role = _resolve_ai_role_label(ai_role_raw, normalized_available, key_to_label)
            relaxed_ai_accept = (not can_use_deterministic and top_score <= 1 and ai_conf >= 0.45)
            missing_conf_accept = (not can_use_deterministic and ai_conf == 0.0)
            if (
                resolved_ai_role
                and (ai_conf >= 0.72 or relaxed_ai_accept or missing_conf_accept)
            ):
                role = resolved_ai_role
                role_found = True
                message = ""
                if ai_conf > 0.0:
                    normalized_conf = max(normalized_conf, ai_conf)
                else:
                    normalized_conf = max(normalized_conf, 0.62)
                    warnings.append("AI role confidence missing; accepted constrained role output.")
            elif ai_role_raw:
                warnings.append(f"AI returned unmatched/low-confidence role: {ai_role_raw}")
            elif ai_reason:
                warnings.append(ai_reason)
        else:
            warnings.append("AI JD text fallback returned no result.")

    if (not role_found) and use_ai_fallback and source_file_bytes:
        ai_doc_data = _ai_jd_role_match_from_document(
            source_file_bytes,
            source_mime_type or "application/pdf",
            [item["role"] for item in role_scores],
        )
        if ai_doc_data:
            used_ai = True
            ai_role_raw = ai_doc_data.get("role")
            ai_conf = max(0.0, min(1.0, float(ai_doc_data.get("confidence") or 0.0)))
            ai_reason = ai_doc_data.get("reason") or ""
            resolved_ai_role = _resolve_ai_role_label(ai_role_raw, normalized_available, key_to_label)
            relaxed_doc_accept = (not can_use_deterministic and top_score <= 1 and ai_conf >= 0.40)
            missing_conf_accept = (not can_use_deterministic and ai_conf == 0.0)
            if (
                resolved_ai_role
                and (ai_conf >= 0.68 or relaxed_doc_accept or missing_conf_accept)
            ):
                role = resolved_ai_role
                role_found = True
                message = ""
                if ai_conf > 0.0:
                    normalized_conf = max(normalized_conf, ai_conf)
                else:
                    normalized_conf = max(normalized_conf, 0.60)
                    warnings.append("AI document role confidence missing; accepted constrained role output.")
            elif ai_role_raw:
                warnings.append(f"AI document returned unmatched/low-confidence role: {ai_role_raw}")
            elif ai_reason:
                warnings.append(ai_reason)
        else:
            warnings.append("AI JD document fallback returned no result.")
            warnings.append(
                "AI service unavailable or unreachable. Verify GEMINI_API_KEY and outbound access to Gemini API."
            )

    if not role_found:
        inferred_role, filename_hint = _infer_role_from_filename(source_filename, [item["role"] for item in role_scores])
        if inferred_role:
            role = inferred_role
            role_found = True
            message = ""
            normalized_conf = max(normalized_conf, 0.55)
            if filename_hint:
                warnings.append(f"Role inferred from filename hint: {filename_hint}")

    if (not role_found) and can_use_deterministic and top_score >= 2:
        role = role_scores[0]["role"]
        role_found = True
        message = ""
        normalized_conf = max(normalized_conf, 0.42)
        warnings.append("Role selected via deterministic best-effort fallback.")

    log.info(
        "JD role matching completed | role_found=%s role=%s used_ai=%s score=%s margin=%s confidence=%.3f",
        role_found,
        role,
        used_ai,
        top_score,
        margin,
        normalized_conf,
    )

    return {
        "role": role,
        "role_found": role_found,
        "message": message,
        "confidence": {
            "score": top_score,
            "normalized": round(normalized_conf, 3),
            "margin": round(float(margin), 3),
        },
        "top_matches": top_matches,
        "warnings": warnings,
        "used_ai": used_ai,
    }
