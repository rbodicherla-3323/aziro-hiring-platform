def allowed_file_extension(filename: str) -> bool:
    ext = get_file_extension(filename)
    return bool(ext and ext in ALLOWED_EXTENSIONS)


def get_file_extension(filename: str) -> str:
    if "." not in str(filename or ""):
        return ""
    return str(filename).rsplit(".", 1)[1].lower().strip()


def get_mime_type_for_filename(filename: str) -> str:
    ext = get_file_extension(filename)
    return {
        "pdf": "application/pdf",
        "docx": "application/vnd.openxmlformats-officedocument.wordprocessingml.document",
        "doc": "application/msword",
        "txt": "text/plain",
    }.get(ext, "application/octet-stream")


def _normalize_label(value: str) -> str:
    text = html.unescape(str(value or ""))
    text = text.replace("\u00e2\u20ac\u201c", "-").replace("\u00e2\u20ac\u201d", "-")
    text = text.replace("\u2013", "-").replace("\u2014", "-")
    text = re.sub(r"\s*-\s*", "-", text)
    text = re.sub(r"\s*\+\s*", "+", text)
    return " ".join(text.split()).strip().lower()

def _safe_json_from_text(raw: str):
    if not raw:
        return None
    cleaned = str(raw).strip()
    if cleaned.startswith("```"):
        cleaned = re.sub(r"^```(?:json)?", "", cleaned, flags=re.IGNORECASE).strip()
        cleaned = re.sub(r"```$", "", cleaned).strip()
    if cleaned.startswith("{") and cleaned.endswith("}"):
        try:
            return json.loads(cleaned)
        except Exception:
            pass
    match = re.search(r"\{[\s\S]*\}", cleaned)
    if not match:
        return None
    try:
        return json.loads(match.group(0))
    except Exception:
        return None


