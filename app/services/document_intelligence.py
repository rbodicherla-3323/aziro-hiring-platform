def _compact_text(value: str):
    return re.sub(r"[^a-z0-9]+", "", str(value or "").lower())


def _count_hits(text_lower: str, keywords, compact_text: str = ""):
    score = 0
    compact_source = compact_text or _compact_text(text_lower)
    for kw in keywords:
        key = kw.lower().strip()
        if not key:
            continue
        if " " in key or "." in key or "+" in key:
            if key in text_lower:
                score += 1
                continue
            compact_key = _compact_text(key)
            if compact_key and compact_key in compact_source:
                score += 1
        else:
            if re.search(rf"\b{re.escape(key)}\b", text_lower):
                score += 1
                continue
            compact_key = _compact_text(key)
            if compact_key and compact_key in compact_source:
                score += 1
    return score


def _extract_years(text_lower: str):
    matches = re.findall(r"(\d{1,2})\s*(?:\+|plus)?\s*(?:years|yrs|year)", text_lower)
    years = [int(m) for m in matches if m.isdigit()]
    return max(years) if years else None


