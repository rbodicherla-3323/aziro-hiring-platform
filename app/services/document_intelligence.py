def extract_resume_identity(
    text: str,
    filename: str,
    use_ai_fallback: bool = True,
    source_file_bytes: bytes = None,
    source_mime_type: str = None,
    text_quality: float = 1.0,
):
    warnings = []
    quality = float(text_quality or 0.0)
    can_use_deterministic = quality >= _MIN_TEXT_QUALITY_FOR_DETERMINISTIC

    emails = _extract_emails(text) if can_use_deterministic else []
    email = max(emails, key=_score_email) if emails else None
    email_conf = 0.92 if email else 0.0

    if can_use_deterministic:
        name, name_conf = _extract_name_candidate(text)
    else:
        name, name_conf = (None, 0.0)
        warnings.append("Resume text quality is low; deterministic extraction disabled.")

    used_ai = False
    if use_ai_fallback and can_use_deterministic and ((not name) or (not email) or name_conf < 0.7):
        ai_data = _ai_resume_extraction(text)
        if ai_data:
            used_ai = True
            ai_name = ai_data.get("name")
            ai_email = ai_data.get("email")
            ai_name_conf = max(0.0, min(1.0, ai_data.get("confidence_name", 0.0)))
            ai_email_conf = max(0.0, min(1.0, ai_data.get("confidence_email", 0.0)))
            if ai_name and (not name or ai_name_conf > (name_conf + 0.08)):
                name = ai_name
                name_conf = ai_name_conf
            if ai_email and (not email or ai_email_conf > (email_conf + 0.04)):
                email = ai_email
                email_conf = ai_email_conf
        else:
            warnings.append("AI resume text fallback returned no result.")

    if use_ai_fallback and source_file_bytes and ((not name) or (not email)):
        ai_doc_data = _ai_resume_extraction_from_document(
            source_file_bytes,
            source_mime_type or get_mime_type_for_filename(filename),
        )
        if ai_doc_data:
            used_ai = True
            ai_name = ai_doc_data.get("name")
            ai_email = ai_doc_data.get("email")
            ai_name_conf = max(0.0, min(1.0, ai_doc_data.get("confidence_name", 0.0)))
            ai_email_conf = max(0.0, min(1.0, ai_doc_data.get("confidence_email", 0.0)))
            if ai_name and (not name or ai_name_conf >= 0.62):
                name = ai_name
                name_conf = max(name_conf, ai_name_conf)
            if ai_email and (not email or ai_email_conf >= 0.62):
                email = ai_email
                email_conf = max(email_conf, ai_email_conf)
        else:
            warnings.append("AI resume document fallback returned no result.")
            warnings.append(
                "AI service unavailable or unreachable. Verify GEMINI_API_KEY and outbound access to Gemini API."
            )

    messages = {}
    if not name:
        messages["name"] = "Name not found in resume."
    if not email:
        messages["email"] = "Email not found in resume."

    overall_conf = round((name_conf + email_conf) / 2.0, 3)
    log.info(
        "Resume extraction completed | file=%s name_found=%s email_found=%s used_ai=%s confidence=%.3f",
        filename,
        bool(name),
        bool(email),
        used_ai,
        overall_conf,
    )

    return {
        "name": name,
        "email": email,
        "name_found": bool(name),
        "email_found": bool(email),
        "messages": messages,
        "confidence": {
            "name": round(name_conf, 3),
            "email": round(email_conf, 3),
            "overall": overall_conf,
        },
        "warnings": warnings,
        "used_ai": used_ai,
    }


