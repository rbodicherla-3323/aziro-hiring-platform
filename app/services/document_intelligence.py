def _decode_bytes_best_effort(file_bytes: bytes):
    warnings = []
    for enc in ("utf-8", "utf-16", "latin-1"):
        try:
            text = file_bytes.decode(enc)
            if text:
                return text, enc, warnings
        except Exception:
            continue
    warnings.append("Unable to decode file bytes with supported text encodings.")
    return "", "unknown", warnings


_PDF_LITERAL_RE = re.compile(r"\((?:\\.|[^()]){1,4000}\)", re.DOTALL)
_PDF_HEX_RE = re.compile(r"<[0-9A-Fa-f\s]{2,8000}>", re.DOTALL)


def _iter_pdf_literal_tokens(blob_text: str, max_len: int = 6000):
    text = str(blob_text or "")
    tokens = []
    i = 0
    n = len(text)
    while i < n:
        if text[i] != "(":
            i += 1
            continue
        start = i
        i += 1
        depth = 1
        while i < n and depth > 0 and (i - start) <= max_len:
            ch = text[i]
            if ch == "\\":
                i += 2
                continue
            if ch == "(":
                depth += 1
            elif ch == ")":
                depth -= 1
            i += 1
        if depth == 0:
            tokens.append(text[start:i])
        else:
            i = start + 1
    return tokens


def _looks_like_text_fragment(value: str):
    text = str(value or "").strip()
    if len(text) < 2:
        return False
    printable_ratio = sum(1 for ch in text if ch.isprintable() and ch != "\x00") / max(len(text), 1)
    if printable_ratio < 0.8:
        return False
    alpha = sum(1 for ch in text if ch.isalpha())
    digit = sum(1 for ch in text if ch.isdigit())
    signal = alpha + digit + text.count("@") + text.count(".") + text.count("+") + text.count("/") + text.count("#")
    return signal / max(len(text), 1) >= 0.25


def _decode_pdf_literal_token(token: str):
    value = str(token or "")
    if len(value) < 2 or not value.startswith("(") or not value.endswith(")"):
        return ""
    source = value[1:-1]
    out = []
    i = 0
    while i < len(source):
        ch = source[i]
        if ch != "\\":
            out.append(ch)
            i += 1
            continue

        i += 1
        if i >= len(source):
            break

        esc = source[i]
        if esc in "nrtbf":
            out.append({"n": "\n", "r": "\r", "t": "\t", "b": "\b", "f": "\f"}[esc])
            i += 1
            continue
        if esc in "()\\":
            out.append(esc)
            i += 1
            continue
        if esc in "\n\r":
            if esc == "\r" and (i + 1) < len(source) and source[i + 1] == "\n":
                i += 2
            else:
                i += 1
            continue
        if esc.isdigit() and esc < "8":
            oct_digits = esc
            i += 1
            count = 1
            while i < len(source) and count < 3 and source[i].isdigit() and source[i] < "8":
                oct_digits += source[i]
                i += 1
                count += 1
            try:
                out.append(chr(int(oct_digits, 8)))
            except Exception:
                pass
            continue

        out.append(esc)
        i += 1

    return "".join(out)


def _decode_pdf_hex_token(token: str):
    value = str(token or "")
    if len(value) < 2 or not value.startswith("<") or not value.endswith(">"):
        return ""
    raw_hex = re.sub(r"[^0-9A-Fa-f]", "", value[1:-1])
    if not raw_hex:
        return ""
    if len(raw_hex) % 2 == 1:
        raw_hex += "0"
    try:
        data = bytes.fromhex(raw_hex)
    except Exception:
        return ""

    if not data:
        return ""
    if data.startswith(b"\xfe\xff"):
        return data[2:].decode("utf-16-be", errors="ignore")
    if data.startswith(b"\xff\xfe"):
        return data[2:].decode("utf-16-le", errors="ignore")
    if data.count(0) > (len(data) // 4):
        decoded = data.decode("utf-16-be", errors="ignore")
        if decoded:
            return decoded
    for enc in ("utf-8", "latin-1"):
        try:
            decoded = data.decode(enc, errors="ignore")
            if decoded:
                return decoded
        except Exception:
            continue
    return ""


def _is_noise_line(line: str):
    value = str(line or "").strip().lower()
    if not value:
        return True
    if any(pattern in value for pattern in _NOISE_LINE_PATTERNS):
        return True
    if re.search(r"(?:https?://|www\.)\S+", value):
        # Generic URL-only/footer noise; keep only if line appears to contain email.
        if "@" not in value:
            return True
    return False


def _repair_fragmented_pdf_text(text: str):
    if not text:
        return ""

    lines = str(text).splitlines()
    merged = []
    i = 0
    while i < len(lines):
        current = str(lines[i]).strip()
        if not current:
            merged.append("")
            i += 1
            continue

        if len(current) == 1 and re.match(r"[A-Za-z0-9@._+\-/]", current):
            parts = [current]
            j = i + 1
            while j < len(lines):
                nxt = str(lines[j]).strip()
                if not nxt:
                    break
                if len(nxt) == 1 and re.match(r"[A-Za-z0-9@._+\-/]", nxt):
                    parts.append(nxt)
                    j += 1
                    continue
                break
            if len(parts) >= 3:
                merged.append("".join(parts))
                i = j
                continue
        merged.append(current)
        i += 1

    repaired = []
    for raw_line in merged:
        line = re.sub(r"\s+", " ", raw_line).strip()
        if not line:
            continue

        line = re.sub(r"(?<=[a-z])(?=[A-Z])", " ", line)

        if _is_noise_line(line):
            continue

        # Join common tokenized punctuation around words/emails.
        line = re.sub(r"(?<=\w)\s*@\s*(?=\w)", "@", line)
        line = re.sub(r"(?<=\w)\s*\.\s*(?=\w)", ".", line)
        line = re.sub(r"(?<=\w)\s*-\s*(?=\w)", "-", line)
        line = re.sub(r"(?<=\w)\s*/\s*(?=\w)", "/", line)
        line = re.sub(r"(?<=\w)\s*\+\s*(?=\w)", "+", line)

        # Collapse spaced single-letter words: "J o h n D o e" -> "JohnDoe".
        line = re.sub(
            r"\b(?:[A-Za-z]\s+){2,}[A-Za-z]\b",
            lambda match: match.group(0).replace(" ", ""),
            line,
        )

        tokens = line.split()
        if len(tokens) >= 8:
            single_char_ratio = sum(1 for tok in tokens if len(tok) == 1) / len(tokens)
            if single_char_ratio >= 0.72:
                compact = "".join(tokens)
                if _looks_like_text_fragment(compact):
                    line = compact

        repaired.append(line)

    return "\n".join(repaired).strip()


def _extract_pdf_strings_from_blob(blob_text: str):
    chunks = []
    for token in _iter_pdf_literal_tokens(blob_text):
        content = _decode_pdf_literal_token(token)
        content = re.sub(r"\s+", " ", content).strip()
        if _looks_like_text_fragment(content):
            chunks.append(content)
    for token in _PDF_HEX_RE.findall(blob_text):
        content = _decode_pdf_hex_token(token)
        content = re.sub(r"\s+", " ", content).strip()
        if _looks_like_text_fragment(content):
            chunks.append(content)
    return chunks


def _extract_pdf_text_operators(blob_text: str):
    fragments = []
    for bt_block in re.findall(r"BT(.*?)ET", blob_text, flags=re.DOTALL):
        for snippet in re.findall(r"\((?:\\.|[^()]){1,4000}\)\s*Tj", bt_block, flags=re.DOTALL):
            token_match = _PDF_LITERAL_RE.search(snippet)
            if not token_match:
                continue
            decoded = _decode_pdf_literal_token(token_match.group(0))
            if decoded:
                fragments.append(decoded)

        for snippet in re.findall(r"<[0-9A-Fa-f\s]{2,8000}>\s*Tj", bt_block, flags=re.DOTALL):
            token_match = _PDF_HEX_RE.search(snippet)
            if not token_match:
                continue
            decoded = _decode_pdf_hex_token(token_match.group(0))
            if decoded:
                fragments.append(decoded)

        for array_body in re.findall(r"\[(.*?)\]\s*TJ", bt_block, flags=re.DOTALL):
            parts = []
            for token in re.findall(r"\((?:\\.|[^()]){1,4000}\)|<[0-9A-Fa-f\s]{2,8000}>", array_body, flags=re.DOTALL):
                if token.startswith("("):
                    decoded = _decode_pdf_literal_token(token)
                else:
                    decoded = _decode_pdf_hex_token(token)
                if decoded:
                    parts.append(decoded)
            if parts:
                fragments.append("".join(parts))

    cleaned = []
    for item in fragments:
        normalized = re.sub(r"\s+", " ", item).strip()
        if _looks_like_text_fragment(normalized):
            cleaned.append(normalized)
    return cleaned


def _text_quality_score(text: str):
    value = str(text or "")
    if not value:
        return 0.0

    total = len(value)
    if total < 20:
        return 0.0

    printable_ratio = sum(1 for ch in value if ch.isprintable() or ch in "\r\n\t") / max(total, 1)
    control_ratio = sum(1 for ch in value if ord(ch) < 32 and ch not in "\r\n\t") / max(total, 1)
    alpha_ratio = sum(1 for ch in value if ch.isalpha()) / max(total, 1)
    symbol_ratio = sum(1 for ch in value if (not ch.isalnum()) and (not ch.isspace())) / max(total, 1)

    words = re.findall(r"[A-Za-z]{3,}", value)
    word_like = [w for w in words if re.search(r"[aeiou]", w.lower())]
    word_density = len(word_like) / max(total / 12.0, 1.0)
    vowel_ratio = len(word_like) / max(len(words), 1)

    lower = value.lower()
    signal_terms = [
        "experience", "education", "skills", "profile", "summary", "developer",
        "engineer", "python", "java", "javascript", "project", "email", "phone",
        "responsibilities", "requirements", "job", "qualification",
    ]
    signal_hits = sum(1 for term in signal_terms if term in lower)

    common_terms = [
        "and", "with", "for", "the", "from", "years", "work", "role", "team",
        "design", "development", "testing", "candidate", "knowledge",
        "experience", "responsible", "skills", "education", "contact", "email",
        "resume", "objective", "software", "application", "project",
    ]
    common_hits = sum(1 for term in common_terms if re.search(rf"\b{re.escape(term)}\b", lower))

    score = 0.0
    score += max(0.0, min(1.0, printable_ratio)) * 0.10
    score += max(0.0, min(1.0, alpha_ratio / 0.35)) * 0.15
    score += max(0.0, min(1.0, word_density)) * 0.22
    score += max(0.0, min(1.0, vowel_ratio / 0.65)) * 0.18
    score += max(0.0, min(1.0, signal_hits / 6.0)) * 0.20
    score += max(0.0, min(1.0, common_hits / 8.0)) * 0.15
    score *= max(0.0, 1.0 - min(0.95, control_ratio * 8.0))
    score *= max(0.0, 1.0 - min(0.85, symbol_ratio * 1.6))

    if signal_hits <= 1 and common_hits <= 2:
        score = min(score, 0.12)

    return round(max(0.0, min(1.0, score)), 4)


def _pdf_binary_profile(file_bytes: bytes):
    payload = bytes(file_bytes or b"")
    image_count = len(re.findall(rb"/Subtype\s*/Image", payload))
    font_count = len(re.findall(rb"/Font\b", payload))
    tounicode_count = len(re.findall(rb"/ToUnicode\b", payload))
    flate_count = len(re.findall(rb"/FlateDecode\b", payload))
    dct_count = len(re.findall(rb"/DCTDecode\b", payload))
    ascii85_count = len(re.findall(rb"/ASCII85Decode\b", payload))
    return {
        "image_count": image_count,
        "font_count": font_count,
        "tounicode_count": tounicode_count,
        "flate_count": flate_count,
        "dct_count": dct_count,
        "ascii85_count": ascii85_count,
    }


def _extract_pdf_text_pdfplumber(file_bytes: bytes):
    warnings = []
    try:
        pdfplumber = importlib.import_module("pdfplumber")
    except Exception:
        warnings.append("pdfplumber dependency is not available on server.")
        return "", "pdfplumber_unavailable", warnings

    chunks = []
    try:
        with pdfplumber.open(io.BytesIO(file_bytes)) as pdf:
            for page in pdf.pages:
                chunks.append(page.extract_text() or "")
    except Exception as exc:
        warnings.append(f"Failed to parse PDF content via pdfplumber: {exc}")
        return "", "pdfplumber_failed", warnings

    return _repair_fragmented_pdf_text("\n".join(chunks).strip()), "pdfplumber", warnings


def _extract_pdf_text_pdfminer(file_bytes: bytes):
    warnings = []
    try:
        high_level = importlib.import_module("pdfminer.high_level")
        layout = importlib.import_module("pdfminer.layout")
    except Exception:
        warnings.append("pdfminer.six dependency is not available on server.")
        return "", "pdfminer_unavailable", warnings

    try:
        laparams = layout.LAParams(
            line_margin=0.3,
            char_margin=2.0,
            word_margin=0.2,
            boxes_flow=None,
        )
        text = high_level.extract_text(io.BytesIO(file_bytes), laparams=laparams) or ""
    except Exception as exc:
        warnings.append(f"Failed to parse PDF content via pdfminer: {exc}")
        return "", "pdfminer_failed", warnings

    return _repair_fragmented_pdf_text(text.strip()), "pdfminer", warnings


def _extract_pdf_text(file_bytes: bytes):
    def _extract_pdf_text_basic(raw_pdf_bytes: bytes):
        def _decode_stream_variants(stream_data: bytes):
            variants = []

            def _push(blob):
                if isinstance(blob, (bytes, bytearray)) and blob:
                    variants.append(bytes(blob))

            raw = (stream_data or b"").strip(b"\r\n")
            _push(raw)

            # Common PDF stream chain: ASCII85Decode -> FlateDecode.
            for candidate in list(variants):
                for adobe in (True, False):
                    try:
                        decoded = base64.a85decode(
                            candidate,
                            adobe=adobe,
                            ignorechars=b" \t\r\n\x0b\x0c",
                        )
                        _push(decoded)
                    except Exception:
                        continue

            # Flate decode on raw and ASCII85-decoded variants.
            for candidate in list(variants):
                for wbits in (zlib.MAX_WBITS, -zlib.MAX_WBITS, zlib.MAX_WBITS | 32):
                    try:
                        inflated = zlib.decompress(candidate, wbits)
                        _push(inflated)
                    except Exception:
                        continue

            # Deduplicate while preserving order.
            out = []
            seen = set()
            for blob in variants:
                if blob in seen:
                    continue
                seen.add(blob)
                out.append(blob)
            return out

        candidates = []

        # Pass 1: parse plain-text strings from entire file.
        root_blob = raw_pdf_bytes.decode("latin-1", errors="ignore")
        candidates.extend(_extract_pdf_text_operators(root_blob))
        candidates.extend(_extract_pdf_strings_from_blob(root_blob))

        # Pass 2: parse strings from stream objects, including Flate decompression.
        for match in re.finditer(rb"stream\r?\n(.*?)\r?\nendstream", raw_pdf_bytes, re.DOTALL):
            stream_data = match.group(1)
            blobs = _decode_stream_variants(stream_data)
            for blob in blobs:
                try:
                    decoded = blob.decode("latin-1", errors="ignore")
                except Exception:
                    continue
                candidates.extend(_extract_pdf_text_operators(decoded))
                candidates.extend(_extract_pdf_strings_from_blob(decoded))

        unique = []
        seen = set()
        for item in candidates:
            key = item.lower()
            if key in seen:
                continue
            seen.add(key)
            unique.append(item)

        raw_text = "\n".join(unique).strip()
        return _repair_fragmented_pdf_text(raw_text)

    profile = _pdf_binary_profile(file_bytes)

    attempts = []

    text_pdfplumber, method_pdfplumber, warn_pdfplumber = _extract_pdf_text_pdfplumber(file_bytes)
    attempts.append((text_pdfplumber, method_pdfplumber, warn_pdfplumber))

    text_pdfminer, method_pdfminer, warn_pdfminer = _extract_pdf_text_pdfminer(file_bytes)
    attempts.append((text_pdfminer, method_pdfminer, warn_pdfminer))

    text_basic = _extract_pdf_text_basic(file_bytes)
    attempts.append((text_basic, "pdf_basic", ["Used dependency-free PDF parser fallback."] if text_basic else []))

    best_text = ""
    best_method = "pdf_unknown"
    best_score = 0.0
    best_warnings = []

    for text, method, warn in attempts:
        score = _text_quality_score(text)
        if score > best_score:
            best_score = score
            best_text = text
            best_method = method
            best_warnings = list(warn or [])

    warnings = list(best_warnings)

    if best_text and best_score >= 0.14:
        log.info(
            "PDF extraction selected method=%s quality=%.3f profile=%s",
            best_method,
            best_score,
            profile,
        )
        return best_text, best_method, warnings

    if profile["image_count"] > 0 and profile["font_count"] == 0:
        warnings.append("PDF appears image-only; non-OCR extraction may not recover text.")
    elif profile["image_count"] > max(2, profile["font_count"] * 2):
        warnings.append("PDF appears image-heavy; extracted text quality is low.")
    elif profile["tounicode_count"] == 0:
        warnings.append("PDF font mapping is limited (no ToUnicode); extraction quality may be poor.")

    if best_text:
        warnings.append("Extracted PDF text is low quality and may be unusable.")
        return best_text, best_method, warnings

    warnings.append("No extractable text found in PDF.")
    return "", "pdf_unreadable", warnings


def _extract_docx_text(file_bytes: bytes):
    warnings = []
    parts = []
    xml_targets = {"word/document.xml"}
    try:
        with zipfile.ZipFile(io.BytesIO(file_bytes)) as archive:
            for name in archive.namelist():
                lower = name.lower()
                if lower in xml_targets or lower.startswith("word/header") or lower.startswith("word/footer"):
                    try:
                        parts.append(archive.read(name))
                    except Exception:
                        continue
    except Exception as exc:
        warnings.append(f"Unable to open DOCX archive: {exc}")
        return "", "docx_parse_failed", warnings

    if not parts:
        warnings.append("No extractable XML parts found in DOCX.")
        return "", "docx_empty", warnings

    text_segments = []
    ns = {"w": "http://schemas.openxmlformats.org/wordprocessingml/2006/main"}
    for part in parts:
        try:
            root = ElementTree.fromstring(part)
            # Join runs inside each paragraph so fragmented tokens
            # (common in DOCX) are reconstructed into readable lines.
            for paragraph in root.findall(".//w:p", ns):
                run_text = []
                for node in paragraph.findall(".//w:t", ns):
                    if node.text:
                        run_text.append(node.text)
                line = "".join(run_text)
                line = re.sub(r"\s+", " ", line).strip()
                if line:
                    text_segments.append(line)
        except Exception:
            continue

    # Fallback: if paragraph-based extraction yields nothing, use all text nodes.
    if not text_segments:
        for part in parts:
            try:
                root = ElementTree.fromstring(part)
                for node in root.findall(".//w:t", ns):
                    if node.text:
                        token = re.sub(r"\s+", " ", node.text).strip()
                        if token:
                            text_segments.append(token)
            except Exception:
                continue

    text = "\n".join(text_segments).strip()
    if not text:
        warnings.append("DOCX text nodes were present but empty.")
    return text, "docx_xml", warnings


def _extract_doc_text(file_bytes: bytes):
    text, encoding, warnings = _decode_bytes_best_effort(file_bytes)
    warnings.append("Legacy .doc parsing is best effort and may be incomplete.")
    # Heuristic: if too many replacement chars, treat as weak extraction.
    replacement_ratio = (text.count("\ufffd") / max(len(text), 1)) if text else 1.0
    if replacement_ratio > 0.1:
        warnings.append("Legacy .doc content appears partially unreadable.")
    return text, f"doc_best_effort_{encoding}", warnings


def extract_text_from_file(filename: str, file_bytes: bytes):
    ext = get_file_extension(filename)
    if ext not in ALLOWED_EXTENSIONS:
        return {
            "text": "",
            "file_type": ext or "unknown",
            "extraction_method": "unsupported",
            "text_length": 0,
            "text_quality": 0.0,
            "warnings": [f"Unsupported file type: .{ext}" if ext else "Missing file extension."],
            "error": "Unsupported file type",
        }

    warnings = []
    text = ""
    method = "unknown"

    if ext == "pdf":
        text, method, warnings = _extract_pdf_text(file_bytes)
    elif ext == "docx":
        text, method, warnings = _extract_docx_text(file_bytes)
    elif ext == "txt":
        text, encoding, decode_warnings = _decode_bytes_best_effort(file_bytes)
        method = f"txt_decode_{encoding}"
        warnings.extend(decode_warnings)
    else:
        text, method, warnings = _extract_doc_text(file_bytes)

    return {
        "text": text or "",
        "file_type": ext,
        "extraction_method": method,
        "text_length": len(text or ""),
        "text_quality": _text_quality_score(text or ""),
        "warnings": warnings,
        "error": None,
    }


def _normalize_obfuscated_email_text(text: str) -> str:
    if not text:
        return ""
    out = str(text)
    out = re.sub(r"(?i)\[\s*at\s*\]|\(\s*at\s*\)|\{\s*at\s*\}", "@", out)
    out = re.sub(r"(?i)\[\s*dot\s*\]|\(\s*dot\s*\)|\{\s*dot\s*\}", ".", out)
    out = re.sub(r"(?i)(?<=\w)\s+at\s+(?=\w)", "@", out)
    out = re.sub(r"(?i)(?<=\w)\s+dot\s+(?=\w)", ".", out)
    out = re.sub(r"(?<=\w)\s*@\s*(?=\w)", "@", out)
    out = re.sub(r"(?<=\w)\s*\.\s*(?=\w)", ".", out)
    out = re.sub(r"(?<=\w)\s*-\s*(?=\w)", "-", out)
    return out


def _extract_emails(text: str):
    normalized = _normalize_obfuscated_email_text(text)
    pattern = r"[a-zA-Z0-9._%+\-]{1,64}@[a-zA-Z0-9.\-]{1,253}\.[a-zA-Z]{2,24}"
    raw = re.findall(pattern, normalized)

    if not raw:
        # Fallback for heavily fragmented PDF text where email chars are spaced.
        relaxed_pattern = r"[a-zA-Z0-9._%+\-\s]{1,80}\s*@\s*[a-zA-Z0-9.\-\s]{1,120}\s*\.\s*[a-zA-Z]{2,12}"
        relaxed_hits = re.findall(relaxed_pattern, normalized)
        for item in relaxed_hits:
            compact = re.sub(r"\s+", "", item or "")
            if re.fullmatch(pattern, compact or ""):
                raw.append(compact)

    unique = []
    seen = set()
    for value in raw:
        item = value.strip().strip(".,;:!?)(")
        lower = item.lower()
        local, _, domain = lower.partition("@")
        if not local or not domain:
            continue
        if len(local) > 64 or len(domain) > 253:
            continue
        if ".." in local or ".." in domain:
            continue
        if local.startswith(".") or local.endswith("."):
            continue
        if domain.startswith(".") or domain.endswith("."):
            continue
        if lower in seen:
            continue
        seen.add(lower)
        unique.append(item)
    return unique


def _score_email(email: str):
    email = str(email or "").strip()
    if "@" not in email:
        return -999
    local, domain = email.split("@", 1)
    local_lower = local.lower()
    score = 0
    if local_lower not in _GENERIC_EMAIL_PREFIXES:
        score += 3
    if any(ch.isdigit() for ch in local):
        score -= 1
    if "." in local or "_" in local:
        score += 1
    if domain.lower().endswith((".edu", ".ac.in", ".org")):
        score += 1
    if len(local) < 3:
        score -= 1
    return score


def _line_is_name_candidate(line: str):
    line = str(line or "").strip()
    if not line:
        return False
    lower = line.lower()
    if "@" in line or "http://" in lower or "https://" in lower:
        return False
    if _is_noise_line(line):
        return False
    if any(ext in lower for ext in (".com", ".net", ".org", ".io", ".in")):
        return False
    if re.search(r"\babout\s+me\b", lower):
        return False
    if any(keyword in lower for keyword in _SECTION_KEYWORDS):
        return False
    if len(line) > 64:
        return False
    if sum(ch.isdigit() for ch in line) > 1:
        return False

    tokens = re.findall(r"[A-Za-z][A-Za-z.'\-]*", line)
    if len(tokens) < 2 or len(tokens) > 5:
        return False
    alpha_chars = sum(ch.isalpha() for ch in line)
    if alpha_chars < 4:
        return False
    return True


def _score_name_line(line: str, position: int):
    tokens = re.findall(r"[A-Za-z][A-Za-z.'\-]*", line)
    score = 0
    if position < 2:
        score += 35
    elif position < 5:
        score += 25
    elif position < 10:
        score += 12
    else:
        score += 6

    if len(tokens) in (2, 3):
        score += 32
    elif len(tokens) == 4:
        score += 18
    else:
        score += 8

    if all((t[:1].isupper() or t.isupper()) for t in tokens):
        score += 18

    if any(len(t) == 1 for t in tokens):
        score -= 12

    if sum(ch.isdigit() for ch in line) > 0:
        score -= 20

    return max(0, min(score, 100))


def _extract_name_candidate(text: str):
    def _trim_name_suffix_sections(value: str):
        line = re.sub(r"\s+", " ", str(value or "")).strip(" ,|-")
        if not line:
            return ""
        lower = line.lower()
        for phrase in _NAME_SECTION_PHRASES:
            marker = f" {phrase}"
            pos = lower.find(marker)
            if pos > 0:
                line = line[:pos].strip(" ,|-")
                lower = line.lower()
        line = re.sub(
            r"\s+(?:about|profile|summary|objective|contact|experience|skills|projects|education)\s*$",
            "",
            line,
            flags=re.IGNORECASE,
        ).strip(" ,|-")
        return line

    lines = []
    raw_lines = []
    for ln in str(text or "").splitlines():
        raw = re.sub(r"\s+", " ", ln).strip(" ,|-")
        if not raw:
            continue
        trimmed = _trim_name_suffix_sections(raw)
        if not trimmed:
            continue
        lines.append(trimmed)
        raw_lines.append(raw)

    def _is_single_name_word(value: str):
        token = str(value or "").strip()
        if not re.fullmatch(r"[A-Za-z][A-Za-z.'\-]{1,24}", token):
            return False
        lower = token.lower()
        if lower in _SECTION_KEYWORDS:
            return False
        if _is_noise_line(token):
            return False
        return True

    candidates = [(line, idx) for idx, line in enumerate(lines[:40])]
    for idx in range(min(len(lines) - 1, 20)):
        first = lines[idx]
        second = lines[idx + 1]
        if _is_single_name_word(first) and _is_single_name_word(second):
            candidates.append((f"{first} {second}", idx))
            continue

        # If a heading was attached to the first line (e.g. "MOHIT ABOUT ME"),
        # pair the cleaned first-name token with an uppercase surname token from next line.
        raw_first = raw_lines[idx] if idx < len(raw_lines) else ""
        raw_first_lower = raw_first.lower()
        if _is_single_name_word(first) and any(phrase in raw_first_lower for phrase in _NAME_SECTION_PHRASES):
            m = re.match(r"([A-Za-z][A-Za-z.'\-]{1,24})\b", second)
            if m:
                surname = m.group(1)
                if surname.isupper() and surname.lower() not in _SECTION_KEYWORDS:
                    candidates.append((f"{first} {surname}", idx))

    best_name = None
    best_score = 0
    for line, idx in candidates:
        if not _line_is_name_candidate(line):
            continue
        score = _score_name_line(line, idx)
        if score > best_score:
            best_name = line
            best_score = score
    confidence = round(best_score / 100.0, 3)
    if best_name and best_score >= 60:
        return best_name, confidence
    return None, confidence


